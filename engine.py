def ask_azm(user_input: str):
    messages = [
        {"role": "system", "content": SYSTEM_PROMPT},
        {"role": "user", "content": user_input},
    ]

    response = client.chat.completions.create(
        model="nuha-2.0",
        messages=messages,
        temperature=0.1,
        max_tokens=600,
        stream=False,
    )

